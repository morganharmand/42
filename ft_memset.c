#include "libft.h"

void	*ft_memset(void *b, int c, size_t n)
{
	unsigned char	*ptr;

	ptr = (unsigned char *)b;
	while (n > 0)
	{
		ptr[n - 1] = (unsigned char)c;
		n--;
	}
	return (b);
}
